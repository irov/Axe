#	include "pch.hpp"

#	include <Axe/Exception.hpp>

#	include <Axe/ArchiveWrite.hpp>
#	include <Axe/ArchiveRead.hpp>

namespace Axe
{
	//////////////////////////////////////////////////////////////////////////
	LocalException::LocalException()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	LocalException::LocalException( const std::string & _message )
		: m_message(_message)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void LocalException::rethrow() const
	{
		throw *this;
	}
	//////////////////////////////////////////////////////////////////////////
	void LocalException::write( ArchiveWrite & _aw ) const
	{
		_aw << m_message;
	}
	//////////////////////////////////////////////////////////////////////////
	void LocalException::read( ArchiveRead & _ar )
	{
		_ar >> m_message;
	}
	//////////////////////////////////////////////////////////////////////////
	StdException::StdException( const std::string & _message )
		: LocalException(_message)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void StdException::rethrow() const
	{
		throw *this;
	}
	//////////////////////////////////////////////////////////////////////////
	void UnknownException::rethrow() const
	{
		throw *this;
	}
	//////////////////////////////////////////////////////////////////////////
	void UnknownException::write( ArchiveWrite & _ar ) const
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void UnknownException::read( ArchiveRead & _ar )
	{
	}
}